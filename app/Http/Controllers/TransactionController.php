    public function my_transactions($id){

        $user_id;
        try{
            $user_id = decrypt($id);
        } catch(\Exception $e) {
            report($e);
            abort(505);
        }

        $user = User::findOrFail($user_id);
        $payouts = $user->payouts;


        return view("transactions", compact('payouts'));
   }



